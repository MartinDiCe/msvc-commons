package com.diceprojects.msvccommons.persistences.models.enums;

/**
 * Representa el estado de una entidad en el sistema.
 * Puede ser usado para indicar si una entidad está activa o inactiva.
 */
public enum EntityStatus {
    /**
     * Indica que la entidad está activa.
     */
    Active,

    /**
     * Indica que la entidad está inactiva.
     */
    Inactive
}